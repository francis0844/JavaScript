// switch


// Evaluating Variable
const grade = 'D'; //Switch case are checking very strictly

switch(grade){
    case 'A':
        console.log('You got A');
        break;
    case 'B':
        console.log('You got B');
        break;
    case 'C':
        console.log('You got C');
        break;
    case 'D':
        console.log('You got D');
        break;
    case 'E':
        console.log('You got E');
        break;
    default:
        console.log('not a grade');
}